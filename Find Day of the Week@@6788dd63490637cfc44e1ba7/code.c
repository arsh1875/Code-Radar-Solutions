#include <stdio.h>

int main() {
    int age;

    // Input age and citizenship status
    scanf("%d", &age);

    // Check voting eligibility
    if (age==1 ) {
        printf("Monday\n");
    }
    if (age==2 ) {
        printf("Tuesday\n");
    }
    if (age==3 ) {
        printf("Wednesday\n");
    }
    if (age==4 ) {
        printf("Thrusday\n");
    }
    if (age==5 ) {
        printf("Friday\n");
    }
    if (age==6 ) {
        printf("Saturday\n");
    }
    if (age==7 ) {
        printf("Sunday\n");
    }
    return 0;

}